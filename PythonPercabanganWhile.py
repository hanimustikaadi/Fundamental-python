jumlah_buku = 10
print('Ibu berkata: "baca semua bukumu".')

Jumlah_buku_sudah_dibaca= 0
print(f'Jumlah buku yang sudah di baca {Jumlah_buku_sudah_dibaca}')

while Jumlah_buku_sudah_dibaca < jumlah_buku :
    Jumlah_buku_sudah_dibaca = Jumlah_buku_sudah_dibaca + 1
    print(f'buku ke {Jumlah_buku_sudah_dibaca} sudah di baca')

print(f' jumlah buku sudah dibaca {Jumlah_buku_sudah_dibaca}')